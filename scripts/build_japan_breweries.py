def main():
    features = []
    for i, (bid, name, name_reading, prefecture, address, lng, lat, btype) in enumerate(BREWERIES):
        features.append({
            "type": "Feature",
            "geometry": {"type": "Point", "coordinates": [lng, lat]},
            "properties": {
                "id": bid,
                "name": name,
                "name_reading": name_reading,
                "prefecture": prefecture,
                "address": address,
                "type": btype,
                "source": "jba",
            },
        })

    geojson = {"type": "FeatureCollection", "features": features}
    out_path = os.path.join(os.path.dirname(__file__), "..", "src", "data", "japan_breweries.json")
    with open(out_path, "w", encoding="utf-8") as f:
        json.dump(geojson, f, ensure_ascii=False, indent=2)
    print(f"Wrote {len(features)} breweries to {out_path}")

if __name__ == "__main__":
    main()
